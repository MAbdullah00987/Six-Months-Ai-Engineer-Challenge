

#Operators in Programming
#Definition of Operators
#Operators are special symbols or keywords that perform operations on one or more operands (values or variables). They tell the compiler or interpreter to perform specific mathematical, relational, or logical manipulations and return a result.

#All Types of Operators
#1. Arithmetic Operators
#Perform mathematical calculations.
#+ (Addition)
##- (Subtraction)
#* (Multiplication)
#/ (Division)
##% (Modulus - remainder)
#** (Exponentiation)

#2. Relational/Comparison Operators
#Compare two values and return true or false.
#== (Equal to)
#!= (Not equal to)
#> (Greater than)
#< (Less than)
#>= (Greater than or equal to)
#<= (Less than or equal to)

#3. Logical Operators
#Combine conditional statements and return boolean results.
#AND (&&, and)
#OR (||, or)
#NOT (!, not)

##4. Assignment Operators
#Assign values to variables.
#= (Simple assignment)
#+= (Add and assign)
#-= (Subtract and assign)
#*= (Multiply and assign)
#/= (Divide and assign)

#5. Bitwise Operators
#Perform operations on binary representations.
#& (AND)
#| (OR)
#^ (XOR)
#~ (NOT)
#<< (Left shift)
#>> (Right shift)

#6. Unary Operators
#Operate on a single operand.
#++ (Increment)
#-- (Decrement)
#- (Negation)
#+ (Unary plus)

#7. Ternary Operator
#Conditional operator with three operands.
#? : (condition ? true_value : false_value)

#8. Membership Operators (in languages like Python)
#Test for membership in sequences.
#in
#not in

#9. Identity Operators (in languages like Python)
#Compare object identity.
#is
#is not
